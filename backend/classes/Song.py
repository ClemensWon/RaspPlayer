class Song:

    def __init__(self, songId, deviceId, songName, genre, duration, likes, skips, album, replays):
        self.songId = songId
        self.deviceId = deviceId
        self.songName = songName
        self.genre = genre
        self.duration = duration
        self.likes = likes
        self.skips = skips
        self.album = album
        self.replays = replays